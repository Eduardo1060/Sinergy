    private void BtApagarGerenciaAActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtApagarGerenciaAActionPerformed
        // TODO add your handling code here:
        
         try {
            // validando dados da interface gr�fica.
            int id = 0;
            if (this.TabelaGerenciaA.getSelectedRow() == -1) {
                throw new Mensagens("Primeiro Selecione um Amigo para APAGAR");
            } else {
                id = Integer.parseInt(this.TabelaGerenciaA.getValueAt(this.TabelaGerenciaA.getSelectedRow(), 0).toString());
            }

            // retorna 0 -> primeiro bot�o | 1 -> segundo bot�o | 2 -> terceiro bot�o
            int resposta_usuario = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja APAGAR este Amigo ?");

            if (resposta_usuario == 0) {// clicou em SIM

                // envia os dados para o Aluno processar
                if (this.objAmigo.DeleteAmigoBD(id)) {

                    // limpa os campos
                    this.CampoNomeGerenciaA.setText("");
                    this.CampoTelefoneGerenciaA.setText("");
                    JOptionPane.showMessageDialog(rootPane, "Amigo Apagado com Sucesso!");

                }
            }
            System.out.println(this.objAmigo.getMinhaLista().toString());

        } catch (Mensagens erro) {
            JOptionPane.showMessageDialog(null, erro.getMessage());
        } finally {
            // atualiza a tabela.
            carregaTabela();
        }
    }//GEN-LAST:event_BtApagarGerenciaAActionPerformed

    public void carregaTabela() {

        DefaultTableModel modelo = (DefaultTableModel) this.TabelaGerenciaA.getModel();
        modelo.setNumRows(0);

        ArrayList<Amigo> minhalista = new ArrayList<>();
        minhalista = objAmigo.getMinhaLista();

        for (Amigo a : minhalista) {
            modelo.addRow(new Object[]{
                a.getId(),
                a.getNome(),
                a.getTelefone(),

            });
        }
    }
