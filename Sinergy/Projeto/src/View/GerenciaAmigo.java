    private void BtAlterarGerenciaAActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtAlterarGerenciaAActionPerformed
        // TODO add your handling code here:
        try {
            // recebendo e validando dados da interface gr�fica.
            int id = 0;
            String nome = "";
            String telefone = "";

            if (this.CampoNomeGerenciaA.getText().length() < 2) {
                throw new Mensagens("Nome deve conter ao menos 2 caracteres.");
            } else {
                nome = this.CampoNomeGerenciaA.getText();
            }

            if (this.CampoTelefoneGerenciaA.getText().length() != 11) {
                throw new Mensagens("Numero deve ter ddd.");
            } else {
                telefone = this.CampoTelefoneGerenciaA.getText();
            }

            if (this.TabelaGerenciaA.getSelectedRow() == -1) {
                throw new Mensagens("Primeiro Selecione um Aluno para Alterar");
            } else {
                id = Integer.parseInt(this.TabelaGerenciaA.getValueAt(this.TabelaGerenciaA.getSelectedRow(), 0).toString());
            }

            // envia os dados para o Aluno processar
            if (this.objAmigo.UpdateAmigoBD(nome,telefone,id)) {

                // limpa os campos
                this.CampoNomeGerenciaA.setText("");
                this.CampoTelefoneGerenciaA.setText("");
                JOptionPane.showMessageDialog(rootPane, "Amigo Alterado com Sucesso!");

            }
            System.out.println(this.objAmigo.getMinhaLista().toString());
        } catch (Mensagens erro) {
            JOptionPane.showMessageDialog(null, erro.getMessage());
        } catch (NumberFormatException erro2) {
            JOptionPane.showMessageDialog(null, "Informe um n�mero.");
        } finally {
            carregaTabela(); // atualiza a tabela.
        }
   
    }//GEN-LAST:event_BtAlterarGerenciaAActionPerformed

